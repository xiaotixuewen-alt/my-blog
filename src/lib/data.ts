这听起来简单，但它的意义极其深远。

在专利制度出现之前，发明家面临一个两难困境：公开技术会让竞争对手受益，保密技术又无法获得回报。专利法完美解决了这个问题——公开换保护，保护换回报。

从此，发明变成了一门可以赚钱的职业。瓦特靠蒸汽机专利成为了富翁，哈格里夫斯靠珍妮纺纱机改变了命运。

当创新可以被定价，创新的供给才会源源不断。

